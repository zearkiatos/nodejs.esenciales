/**
 * Function for sum two numbers
 * @param {number} a
 * @param {number} b
 */
function sum(a, b) {
  return a + b;
}

/**
 * Function for diff two numbers
 * @param {number} a
 * @param {number} b
 */
function diff(a, b) {
  return a - b;
}

/**
 * Function for get the product of two products.
 * @param {number} a
 * @param {number} b
 */
function prod(a, b) {
  return a * b;
}

function div(a, b) {
  return a / b;
}

module.exports = {
  sum,
  diff,
  prod,
  div,
};
